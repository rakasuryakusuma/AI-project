celcius = float(input("Input your temperature in Celcius: "))

reamur = 4/5*(celcius)
print("Here's your temperature in Reamur: ", reamur)
farenheit = 9/5*(celcius)+32
print("Here's your temperature in Fahrenheit: ", farenheit)
kelvin = celcius + 273
print("Here's your temperatur in Kelvin: ", kelvin)